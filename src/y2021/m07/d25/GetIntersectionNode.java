public class GetIntersectionNode {

    public static class ListNode {
        int val;
        ListNode next;

        ListNode(int x) {
            val = x;
            next = null;
        }
    }

    public static void main(String[] args) {
        ListNode headInter = new ListNode(3);
        ListNode headA = new ListNode(1);
        headA.next = new ListNode(2);
        headA.next.next = headInter;
        ListNode headB = new ListNode(2);
        headB.next = new ListNode(3);
        headA.next.next = headInter;
    }

    /**
     * 思路：假如链表A的长度为5等于 a+c，链表B的长度为8 等于b+c
     * 从头开始依次遍历两个链表，当一个链表遍历完的时候，去遍历另一个链表。这样相当于把两个链表都走了一次。
     * 当遍历到对方链表的时候 ，链表的总长度是一样。所以他们相等的时候就是他们相交的节点，或者是null
     * <p>
     * 时间复杂度O(n)
     * 空间复杂度O(1)
     *
     * @param headA
     * @param headB
     * @return
     */
    public ListNode getIntersectionNode(ListNode headA, ListNode headB) {
        if (headA == null || headB == null) {
            return null;
        }
        ListNode pA = headA;
        ListNode pB = headB;
        while (pA != pB) {
            pA = (pA == null) ? headB : pA.next;
            pB = (pB == null) ? headA : pB.next;
        }
        return pA;
    }
}
